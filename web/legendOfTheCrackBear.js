function over(kind, j, l, extraX, extraY)
{
    //console.log("( " + ((j - 1) * 300 + (extraX * 300) - 1/2 * CCC.width) + ", " + ((i - 1) * 300 + (extraY * 300) - 1/2 * CCC.height) + " )");
    var tileX = ((j - 1) * 300 + (extraX * 300) - 1/2 * CCC.width);
    var tileY = ((l - 1) * 300 + (extraY * 300) - 1/2 * CCC.height);
    //XXX.drawImage(sea, (j - 1) * 300 + (extraX * 300) + X, (i - 1) * 300 + (extraY * 300) + Y, 300, 300);

    //PLAYER LANDSCAPE SENSING
    if (-X <= tileX + 300 && -X > tileX && -Y <= tileY + 300 && -Y > tileY)
    {
        if (kind == "sea" || kind == "sewer" || kind == "swamp"  || kind == "hollow")
        {
            if (!player.flying)
            {
                player.land = false;
                player.water = true;
                if (kind == "sewer" && player.weaponEquipped == "swimming")
                {
                    if (Math.random() < 0.001)
                    {
                        player.skinWorms = true;
                    }
                }
                else if (kind == "swamp" && player.weaponEquipped == "swimming")
                {
                    if (Math.random() < 0.0001 && player.raceName != "Vardan" || Math.random() < 0.00001 && player.raceName == "Vardan")
                    {
                        if (this.internalWarts == false)
                        {
                            this.internalWarts = true;
                            this.internalWartGrowth = 0;
                        }
                    }
                }
                else if (kind == "hollow" && player.weaponEquipped == "swimming")
                {
                    if (player.vamprism != true && player.antidecay != true)
                    {
                        player.decay += 0.06;
                    }
                }
            }
        }
        else if (kind == "landing")
        {
            player.land = true;
            player.water = true;
        }
        else if (kind == "sticky")
        {
            if (player.nonStick == false)
            {
                player.sticky = true;
                if (!player.flying && player.mudwalker != true)
                {
                    player.stunnedTime = 2;
                    player.stunnedI = true;
                }
            }
        }
        else if (kind == "tollBridge")
        {
            player.land = true;
            player.water = true;
            player.toll = true;
        }
    }

    //UNIT LANDSCAPE SENSING
    for (var i = 0; i < ArtificialIntelligenceAccess.length; i++)
    {
        if (-ArtificialIntelligenceAccess[i].X <= tileX + 300 && -ArtificialIntelligenceAccess[i].X > tileX && -ArtificialIntelligenceAccess[i].Y <= tileY + 300 && -ArtificialIntelligenceAccess[i].Y > tileY)
        {
            if (kind == "sea" || kind == "sewer" || kind == "swamp" || kind == "hollow")
            {
                ArtificialIntelligenceAccess[i].water = true;
                ArtificialIntelligenceAccess[i].land = false;

                if (kind == "hollow" && ArtificialIntelligenceAccess[i].flying != true && ArtificialIntelligenceAccess[i].resistances.indexOf("hollow") == -1 && ArtificialIntelligenceAccess[i].nonStick != true)
                {
                    ArtificialIntelligenceAccess[i].healthMAX -= 0.06;
                    ArtificialIntelligenceAccess[i].health -= 0.06;
                    ArtificialIntelligenceAccess[i].killNotByPlayer = true;
                }
            }
            else if (kind == "sticky")
            {
                if (ArtificialIntelligenceAccess[i].nonStick == false)
                {
                    ArtificialIntelligenceAccess[i].sticky = true;
                    if (ArtificialIntelligenceAccess[i].underground != true && ArtificialIntelligenceAccess[i].flying != true && ArtificialIntelligenceAccess[i].resistances.indexOf("sticky") == -1)
                    {
                        ArtificialIntelligenceAccess[i].stunned = 0.3;
                    }
                }
            }
            else if (kind == "landing" || kind == "tollBridge")
            {
                ArtificialIntelligenceAccess[i].water = true;
                ArtificialIntelligenceAccess[i].land = true;
            }
        }
    }

}

